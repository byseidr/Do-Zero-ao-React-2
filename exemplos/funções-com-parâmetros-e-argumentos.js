// FUNÇÕES COM PARÂMETROS E ARGUMENTOS ----------------------------------------

// Exemplo 1 - Função com parâmetros e argumentos
// Na declaração de uma função, as variáveis definidas no topo são chamadas de
// "parâmetros" da função
function soma(a, b) {
    return a + b;
}

// Ao chamar a função e fornecer valores a ela, estes são chamados de
// "argumentos" da função
console.log(soma(2, 3));
